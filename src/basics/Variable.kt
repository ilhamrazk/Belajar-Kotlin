package basics

fun main(args: Array<String>) {

    /*===== var variable (mutable)=====*/
    var namaJalan   : String
    var nomorJalan  : String

    namaJalan   = "Veteran"
    nomorJalan  = " 50"

    println("Jalan $namaJalan / $nomorJalan")

    /*===== val variable (immutable)=====*/
    val kodePos     : Int

    kodePos     = 1456789
    println("Kode Pos : $kodePos")
}